import pandas as pd

# Определите путь к исходному и целевому файлам CSV
input_csv = 'data.csv'  # Замените на ваш исходный путь к файлу
output_csv = 'data_comb.csv'  # Замените на путь к файлу, в который будут сохранены изменения

# Читаем CSV-файл, пропуская первые 14225 строк
